import { createRequire } from 'node:module'
import { writeFileSync } from 'node:fs'

const require = createRequire(import.meta.url)

/** @param {string} name */
function collectPackageTree(name, seen = new Set()) {
  if (seen.has(name)) return seen
  seen.add(name)

  let pkgPath
  try {
    pkgPath = require.resolve(`${name}/package.json`)
  } catch {
    return seen
  }

  const pkg = require(pkgPath)
  // Optional deps include platform native bindings (android, darwin, etc.) that are
  // not installed on Vercel's Linux builders and must not be statically imported.
  for (const dep of Object.keys(pkg.dependencies ?? {})) {
    collectPackageTree(dep, seen)
  }

  return seen
}

/** @param {string} name */
function canImport(name) {
  if (name.includes('/binding-')) return false
  if (name.startsWith('lightningcss-')) return false
  try {
    require.resolve(name)
    return true
  } catch {
    return false
  }
}

const roots = [
  'remix/node-tsx',
  '@remix-run/assets',
  '@remix-run/node-tsx',
  'remix/router',
  'remix/middleware/static',
  'remix/middleware/render',
  'remix/response/html',
  'remix/ui/server',
]

const packages = new Set()
for (const root of roots) {
  const name = root.startsWith('@') ? root.split('/').slice(0, 2).join('/') : root.split('/')[0]
  collectPackageTree(name, packages)
}

const importable = [...packages].filter(canImport).sort()

const lines = [
  '// Generated by scripts/sync-vercel-traces.mjs — do not edit.',
  ...importable.map((pkg) => `import '${pkg}'`),
  '',
]

writeFileSync('api/trace-deps.js', lines.join('\n'))
console.log(`Wrote api/trace-deps.js (${importable.length} packages)`)
