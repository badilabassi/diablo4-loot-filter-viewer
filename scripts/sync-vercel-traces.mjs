import { createRequire } from 'node:module'
import { writeFileSync } from 'node:fs'

const require = createRequire(import.meta.url)

/** @param {string} name */
function collectPackageTree(name, seen = new Set()) {
  if (seen.has(name)) return seen
  seen.add(name)

  let pkgPath
  try {
    pkgPath = require.resolve(`${name}/package.json`)
  } catch {
    return seen
  }

  const pkg = require(pkgPath)
  const deps = {
    ...pkg.dependencies,
    ...pkg.optionalDependencies,
  }

  for (const dep of Object.keys(deps)) {
    collectPackageTree(dep, seen)
  }

  return seen
}

const roots = [
  'remix/node-tsx',
  '@remix-run/assets',
  '@remix-run/node-tsx',
  'remix/router',
  'remix/middleware/static',
  'remix/middleware/render',
  'remix/response/html',
  'remix/ui/server',
]

const packages = new Set()
for (const root of roots) {
  const name = root.startsWith('@') ? root.split('/').slice(0, 2).join('/') : root.split('/')[0]
  collectPackageTree(name, packages)
  // Also trace subpath roots (e.g. remix/router -> remix package tree is enough)
}

const lines = [
  '// Generated by scripts/sync-vercel-traces.mjs — do not edit.',
  ...[...packages].sort().map((pkg) => `import '${pkg}'`),
  '',
]

writeFileSync('api/trace-deps.js', lines.join('\n'))
console.log(`Wrote api/trace-deps.js (${packages.size} packages)`)
